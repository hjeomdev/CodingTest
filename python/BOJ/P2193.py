import sys

n = int(sys.stdin.readline().rstrip())

# 1 -> 1 (1)
# 2 -> 10 (1)
# 3 -> 100, 101 (2)
# 4 -> 1000, 1001, 1010 (3)
# 5 -> 10000, 10001, 10010, 10100, 10101 (5)
# 6 -> 100000, 100001, 100010, 100100, 101000, 100101, 101001, 101010 (8)
# f(n) = f(n-1) + f(n-2)

memo = [0] * 91
memo[1] = 1
memo[2] = 1

for i in range(3, n + 1):
    memo[i] = memo[i - 1] + memo[i - 2]

print(memo[n])
